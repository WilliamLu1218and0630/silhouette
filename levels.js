// S12 — 終章   (cube frames + bent rods + oblique camera)
// =============================================================================
function cubeEdgesFromOrigin(ox, oy, oz, size) {
  const s = size;
  const v = (a, b, c) => [ox + a, oy + b, oz + c];
  return [
    [v(0, 0, 0), v(s, 0, 0)], [v(s, 0, 0), v(s, s, 0)],
    [v(s, s, 0), v(0, s, 0)], [v(0, s, 0), v(0, 0, 0)],
    [v(0, 0, s), v(s, 0, s)], [v(s, 0, s), v(s, s, s)],
    [v(s, s, s), v(0, s, s)], [v(0, s, s), v(0, 0, s)],
    [v(0, 0, 0), v(0, 0, s)], [v(s, 0, 0), v(s, 0, s)],
    [v(s, s, 0), v(s, s, s)], [v(0, s, 0), v(0, s, s)],
  ];
}

function genS12() {
  const out = [];
  const scenes = [
    { theta: 45, phi: Math.atan(1 / Math.sqrt(2)) * 180 / Math.PI, grid: 9,
      rods: () => cubeEdgesFromOrigin(0, 0, 0, 6) },
    { theta: 28, phi: 22, grid: 11,
      rods: () => [...cubeEdgesFromOrigin(0, 0, 0, 8), [[0, 0, 0], [8, 8, 8]]] },
    { theta: 35, phi: 26, grid: 13,
      rods: () => [
        ...cubeEdgesFromOrigin(0, 0, 0, 8),
        [[4, 0, 0], [4, 8, 8]],
        [[0, 4, 4], [8, 4, 4]],
      ]},
  ];
  for (let i = 0; i < scenes.length; i++) {
    const s = scenes[i];
    const t = s.theta * Math.PI / 180, p = s.phi * Math.PI / 180;
    const rods = s.rods();
    out.push(L(`S12-12${'ABCDE'[i]}`, s.grid, rods,
      rods.map(path => projectAt(t, p, path))));
  }
  return out;
}

// =============================================================================
// HARD — hand-designed showcase puzzles
// =============================================================================
function genHard() {
  const out = [];

  // ── HARD-1 「目」字 (6 unique L-rods, anamorphic) ─────────────────────────
  // Each rod is a UNIQUE 2-segment axis-aligned compound shape (6 L-corner
  // orientations). Their bend directions immediately tell the player which
  // section of 目 each one belongs to. Coverage overlaps at corners:
  //   • top edge:    rods 1, 2  (both cover top, bend down at opposite ends)
  //   • bottom edge: rods 5, 6
  //   • left vert:   rods 1, 3, 5  (3 stacked verticals)
  //   • right vert:  rods 2, 4, 6
  // Each rod sits in its own gz layer (0/4/8/12/2/10). The 3D scatter forces
  // the player to also hit the right gz when re-placing each rod.
  {
    const t = 15 * Math.PI / 180, p = 10 * Math.PI / 180;
    // All rods coplanar at gz=6 so shared 3D endpoints project to the same
    // screen point — the goal renders as ONE connected 目, not 6 scattered Ls.
    const Z = 6;
    const rods = [
      // 1. ⌜ top-left L (2-seg)        : top edge + upper-left vertical
      [[6, 12, Z], [0, 12, Z], [0,  8, Z]],
      // 2. ⌝ top-right L (2-seg)       : top edge + upper-right vertical
      [[0, 12, Z], [6, 12, Z], [6,  8, Z]],
      // 3. Z spanning (3-seg)          : bar 1 → middle-right vertical → bar 2
      [[0,  8, Z], [6,  8, Z], [6,  4, Z], [0,  4, Z]],
      // 4. I straight (1-seg)          : middle-left vertical
      [[0,  8, Z], [0,  4, Z]],
      // 5. ⌞ bottom-left L (2-seg)     : lower-left vertical + bottom edge
      [[0,  4, Z], [0,  0, Z], [6,  0, Z]],
      // 6. ⌟ bottom-right L (2-seg)    : bottom edge + lower-right vertical
      [[0,  0, Z], [6,  0, Z], [6,  4, Z]],
    ];
    out.push(L('目字', 13, rods, rods.map(r => projectAt(t, p, r))));
  }

  // ── HARD-1 北斗七星 ───────────────────────────────────────────────────────
  // 7 stars placed across the cube so that 4 bowl stars cluster at gz≈0–1
  // while 3 handle stars fly out to gz=7–12. From (θ=25°, φ=15°) all seven
  // collapse to the canonical Big Dipper outline. The "trap": Alioth and
  // Megrez look adjacent in projection but sit 7 cells apart in depth.
  {
    const t = 25 * Math.PI / 180, p = 15 * Math.PI / 180;
    const Dubhe  = [5, 1, 1];
    const Merak  = [5, 4, 1];
    const Phecda = [1, 3, 0];
    const Megrez = [1, 0, 0];
    const Alioth = [2, 2, 7];
    const Mizar  = [2, 4, 11];
    const Alkaid = [0, 5, 12];
    const rods = [
      [Dubhe, Merak],          // bowl right
      [Merak, Phecda],         // bowl bottom
      [Phecda, Megrez],        // bowl left
      [Megrez, Dubhe],         // bowl top (closes)
      [Megrez, Alioth],        // handle 1
      [Alioth, Mizar],         // handle 2
      [Mizar, Alkaid],         // handle 3
    ];
    out.push(L('北斗七星', 13, rods, rods.map(r => projectAt(t, p, r))));
  }

  return out;
}

