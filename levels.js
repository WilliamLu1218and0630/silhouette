// =============================================================================
// Silhouette — Level Bank
// -----------------------------------------------------------------------------
// Each LEVELS[idx] holds a level TEMPLATE with rods at the SOLVED position.
// The engine calls scrambleLevel(level, seed) at load-time to randomly translate
// each rod within grid bounds. The seed lives in the engine, freshly generated
// every loadLevel and reused only when the player presses Reset.
//
// goalPaths coordinate convention (in grid units; engine multiplies by CELL):
//   • Top-down (theta=0, phi=π/2):     goal = (gx, gz)
//   • Front    (theta=0, phi=0):       goal = (gx, gy_inv)  small y = top
//   • Side     (theta=π/2, phi=0):     goal = (-gz, gy_inv)
//   • Oblique: precomputed by projectAt(theta, phi, ...)
// =============================================================================

export const LEVEL_FORMAT_VERSION = 2;

const L = (name, grid, rods, goalPaths) => ({
  version: LEVEL_FORMAT_VERSION,
  name, grid,
  rods: rods.map((p, i) => ({ id: i, path: p })),
  goalPaths,
});

// =============================================================================
// PRNG + scrambler
// =============================================================================
function mulberry32(seed) {
  let s = seed >>> 0;
  return function () {
    s = (s + 0x6D2B79F5) >>> 0;
    let t = s;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

// Translate a rod path within the grid. 2-D paths are also shifted in y
// (range [0, gridSize)) and promoted to 3-D nodes on output.
// Guarantees a non-zero translation when the bbox leaves room for one.
function scrambleRod(path, gridSize, rng) {
  const is2D = path[0].length === 2;
  const norm = path.map(n => is2D ? [n[0], 0, n[1]] : n);
  const mins = [Infinity, Infinity, Infinity];
  const maxs = [-Infinity, -Infinity, -Infinity];
  for (const n of norm) for (let i = 0; i < 3; i++) {
    if (n[i] < mins[i]) mins[i] = n[i];
    if (n[i] > maxs[i]) maxs[i] = n[i];
  }
  const ranges = [
    gridSize - (maxs[0] - mins[0]),
    gridSize - (maxs[1] - mins[1]),
    gridSize - (maxs[2] - mins[2]),
  ];
  const canMove = ranges[0] > 1 || ranges[1] > 1 || ranges[2] > 1;
  for (let attempt = 0; attempt < 32; attempt++) {
    const dx = Math.floor(rng() * ranges[0]) - mins[0];
    const dy = Math.floor(rng() * ranges[1]) - mins[1];
    const dz = Math.floor(rng() * ranges[2]) - mins[2];
    if (canMove && dx === 0 && dy === 0 && dz === 0) continue;
    return norm.map(n => [n[0] + dx, n[1] + dy, n[2] + dz]);
  }
  return norm.map(n => [...n]);
}

// Returns a NEW level with rods scrambled. Preserves goalPaths/grid/name.
export function scrambleLevel(level, seed) {
  const rng = mulberry32(seed);
  return {
    ...level,
    rods: level.rods.map(rod => ({
      id: rod.id,
      path: scrambleRod(rod.path, level.grid, rng),
    })),
  };
}

