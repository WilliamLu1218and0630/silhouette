// =============================================================================
// projectAt — replicates engine projection for oblique-camera goal generation
// =============================================================================
function projectAt(theta, phi, nodes) {
  const sx = Math.sin(theta), cx = Math.cos(theta);
  const sy = Math.sin(phi),   cy = Math.cos(phi);
  const upx = -sy * sx, upy = cy, upz = -sy * cx;
  const vx = -cy * sx, vy = -sy, vz = -cy * cx;
  const rx = vy * upz - vz * upy;
  const ry = vz * upx - vx * upz;
  const rz = vx * upy - vy * upx;
  return nodes.map(n => {
    const wx = n[0], wy = (n[1] || 0), wz = n[2];
    const px = wx * rx + wy * ry + wz * rz;
    const py = wx * upx + wy * upy + wz * upz;
    return [px, -py];
  });
}

